                    <h5><strong>Katarak</strong> adalah lensa mata yang menjadi keruh, sehingga cahaya tidak dapat menembusnya, bervariasi sesuai tingkatannya dari sedikit sampai keburaman total.</h5>
                    <p>
                        Dalam perkembangannya katarak yang terkait dengan usia penderita dapat menyebabkan pengerasan lensa, menyebabkan penderita menderita miopi,
                        berwarna kuning menjadi coklat/putih secara bertahap dan keburaman lensa dapat mengurangi persepsi akan warna biru.
                        Katarak biasanya berlangsung perlahan-lahan menyebabkan kehilangan penglihatan dan berpotensi membutakan jika katarak terlalu tebal.
                        Kondisi ini biasanya memengaruhi kedua mata, tapi hampir selalu satu mata dipengaruhi lebih awal dari yang lain.
                    </p>
                    <ul class="features-list">
                        <li><i class="flaticon-check-mark"></i> Pandangan kabur yang tidak dapat dikoreksi dengan kacamata atau ukuran kacamata yang sering berubah</li>
                        <li><i class="flaticon-check-mark"></i> Warna-warna tampak kusam</li>
                        <li><i class="flaticon-check-mark"></i> Susah melihat di tempat yang terang akibat silau</li>
                        <li><i class="flaticon-check-mark"></i> Kesulitan saat membaca atau mengemudi di malam hari</li>
                    </ul>
                    <p>
                        Sampai saat ini operasi katarak masih menjadi hal yang menakutkan bagi sebagian orang. Namun, dengan perkembangan teknologi yang semakin maju,
                        maka operasi katarak tidak lagi menakutkan karena ada alat dengan teknologi modern yang membuat operasi katarak dapat dilakukan dengan teknik Phaco (baca: Fako)
                        Emulsifikasi dan tanpa membuat sayatan lebar, sehingga operasi menjadi cepat, mudah, dan aman tanpa jahitan.
                    </p>
                    <h5>Apa yang dimaksud dengan Teknik Phaco Emulsifikasi?</h5>
                    <p>
                        Operasi pengangkatan katarak modern dengan menggunakan alat phacoemulsifikasi (awam disebut dengan laser) untuk melunakkan (emulsifikasi) dan mengeluarkan lensa katarak pada saat yang bersamaan. Setelah itu, lensa intra-okuler (IOL) yang dapat dilipat dimasukkan ke dalam mata. Setelah operasi, pasien dapat beraktivitas kembali tanpa penutup mata.
                    </p>
